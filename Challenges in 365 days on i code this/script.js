// selectors
const gallery = document.getElementById('gallery');
const btnPrev = document.getElementById('btn-prev');
const btnNext = document.getElementById('btn-next');

// create gallery
function populateGallery(data) {
    const numColumns = Math.ceil(data.length / 2);
    gallery.style.gridTemplateColumns = `repeat(${numColumns}, 1fr)`;

    // Sort data by REVERSE date 
    data.sort((a, b) => new Date(b.submitted) - new Date(a.submitted));
    
    // create and add image blocks
    data.forEach((item, index) => {
        const link = document.createElement('a');
        link.style.gridColumn = "span 1";
        link.style.gridRow = (index % 3 === 0) ? "span 2" : "span 1";
        
        const img = document.createElement('img');
        img.src = `https://shismqklzntzxworibfn.supabase.co/storage/v1/object/public/previews/${item.previewImage}`;
        img.alt = item.mode;

        const title = document.createElement('p');
        title.innerText = item.mode;

        link.href = `https://icodethis.com/modes/design-to-code/${item.modeId}/submissions/${item.submission_id}`;
        link.target = "_blank";
        link.appendChild(img);
        link.appendChild(title);
        
        gallery.appendChild(link);
    });
}


// gallery nav
function galleryControls() {
    let counter = 0;
    btnPrev.addEventListener('click', () => {
        gallery.scrollBy({
            left: -window.innerWidth,
            behavior: 'smooth'
         });
         counter--;
        btnState(counter)
    });
    btnNext.addEventListener('click', () => {
        gallery.scrollBy({
            left: window.innerWidth,
            behavior: 'smooth'
        });
        counter++;
        btnState(counter)
    });
    
}
function btnState(counter){
    btnPrev.disabled = counter>0 ? false : true;
}

